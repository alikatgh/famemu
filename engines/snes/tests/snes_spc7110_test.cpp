int main() {
    static uint8_t rom[0x600000];
    uint32_t x = 1;
    for (uint32_t i = 0; i < sizeof rom; ++i) { x = x * 1103515245u + 12345u; rom[i] = static_cast<uint8_t>(x >> 16); }

    int failed = 0;
    for (unsigned mode = 0; mode < 3; ++mode) {
        Spc7110Decomp d;
        d.set_rom(rom, sizeof rom);
        d.init(mode, 0, 0);
        for (int i = 0; i < 64; ++i) {
            const uint8_t got = d.read();
            if (got != kGold[mode][i]) {
                std::printf("mode %u byte %d: got %02X expect %02X\n", mode, i, got, kGold[mode][i]);
                failed = 1;
                break;
            }
        }
    }
    std::printf("spc7110 decompressor: %s\n", failed ? "MISMATCH" : "3/3 modes match reference");
    return failed;
}
