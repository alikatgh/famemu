// SPC7110 decompressor faithfulness gate.
//
// Our Spc7110Decomp is a port of byuu/neviksti's reverse-engineered decoder.
// This test drives it over a fixed pseudo-random "data ROM" and compares the
// first 64 output bytes of each decompression mode against golden output
// captured from the ORIGINAL reference implementation (same fake ROM, same
// deterministic LCG). Identical output proves the port has no transcription
